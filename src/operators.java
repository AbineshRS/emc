public class operators {
 public static void main(String args[]){     // operators + - / * %
     int a=10,b=20,c;
     System.out.println(a);
     a++;// post increment
     ++a;//pre increment
     System.out.println(a);
     a+=5;
     System.out.println(a);
     c=a*b;
     System.out.println(c);
 }
}
